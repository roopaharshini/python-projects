# List
li = ["apple", "banana", "cherry"]
for x in li:
    print(x)

# Tuple
tup = ("red", "green", "blue")
for x in tup:
    print(x)

# String
s = "python"
for x in s:
    print(x)

# Dictionary
d = {'a': 100, 'b': 200}
for x in d:
    print("%s  %d" % (x, d[x]))

# Set
set1 = {5, 15, 25}
for x in set1:
    print(x)




